def add(a,b):
    """
    Retourne la somme de a et b.

    Parameters:
        a (float): Le premier nombre.
        b (float): Le deuxième nombre.
    Returns:
        float: La somme de a et b.
    """
    return a + b

def subtract(a,b):
    """
    Retourne la différence de a et b.

    Parameters:
        a (float): Le premier nombre.
        b (float): Le deuxième nombre.
    Returns:
        float: La différence de a et b.
    """
    return a - b

def multiply(a,b):
    """
    Retourne le produit de a et b.

    Parameters:
        a (float): Le premier nombre.
        b (float): Le deuxième nombre.
    Returns:
        float: Le produit de a et b.
    """
    return a ** b

def divide(a,b):
    """
    Retourne le quotient de b et a.

    Parameters:
        a (float): Le premier nombre.
        b (float): Le deuxième nombre.
    Returns:
        float: Le quotient de a et b.
    """
    return a // b
